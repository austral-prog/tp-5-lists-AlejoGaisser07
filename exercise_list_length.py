# Ejercicio 1: Obtener la longitud de una lista

def list_length(lista):
    """
    Retorna la cantidad de elementos en la lista.

    Args:
        lista: Una lista de cualquier tipo de elementos

    Returns:
        Un entero con la cantidad de elementos
    """
    length = len(lista)
    return length

